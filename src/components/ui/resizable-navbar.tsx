export const NavbarButton = ({
  href,
  as: Tag = "a",
  children,
  className,
  variant = "primary",
  ...props
}: {
  href?: string;
  as?: React.ElementType;
  children: React.ReactNode;
  className?: string;
  variant?: "primary" | "secondary" | "dark" | "gradient";
} & (
  | React.ComponentPropsWithoutRef<"a">
  | React.ComponentPropsWithoutRef<"button">
)) => {
  const baseStyles =
    "px-4 py-2 rounded-md text-sm font-bold relative cursor-pointer hover:-translate-y-0.5 transition duration-200 inline-block text-center border border-border bg-card text-card-foreground focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring/50";

  const variantStyles = {
    primary: "shadow-lg shadow-foreground/5",
    secondary:
      "bg-transparent border-border/60 text-muted-foreground shadow-none",
    dark: "bg-foreground text-background border-transparent shadow-lg shadow-foreground/10",
    gradient:
      "border-transparent bg-gradient-to-b from-[var(--primary)] to-[color-mix(in_oklab,var(--primary),transparent_20%)] text-[var(--primary-foreground)] shadow-lg shadow-foreground/10",
  };

  return (
    <Tag
      href={href || undefined}
      className={cn(baseStyles, variantStyles[variant], className)}
      {...props}
    >
      {children}
    </Tag>
  );
};
